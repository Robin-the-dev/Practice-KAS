const hexToDec = (hex) => {
  return parseInt(hex, 16);
}

const getBalance = async (addr) => {
  try {
	const balance = await caver.rpc.klay.getBalance(addr);
	return hexToDec(balance);
  } catch(err) {
	console.log(err);
	return err;
  }
}

const getTokenBalance = async (addr) => {
  try {
	const tokenBalance = await MyKIP7TokenV2.methods.balanceOf(addr).call();
	return tokenBalance;
  } catch(err) {
	console.log(err);
	return err;
  }
}

const transferToken = async (from, to, amount) => {
  try {
	amount = await caver.utils.convertToPeb(amount);
	const isTransferred = await MyKIP7TokenV2.methods.transfer(to, amount).send({from: from, gas: 1000000});
	return isTransferred;
  } catch(err) {
	console.log(err);
	return err;
  }
}

const createAccount = async () => {
  try {
	const account = await caver.kas.wallet.createAccount();
	return account;
  } catch(err) {
	console.log(err);
	return err;
  }
}

app.get('/createaccount', async (req, res) => {
  const account = await createAccount();
  res.status(200).json(account);
});

app.get('/getbalance/:addr', async (req, res) => {
  const address = req.params.addr;

  const balance = await getBalance(address);
  res.status(200).json(balance);
});

app.get('/gettokenbalance/:addr', async (req, res) => {
  const address = req.params.addr;

  const tokenBalance = await getTokenBalance(address);
  res.status(200).json(tokenBalance);
});

app.get('/transfertoken', async (req, res) => {
  const to = req.query.to;
  const from = req.query.from;
  const amount = req.query.amount;
  
  const isTransferred = await transferToken(from, to, amount);
  
  if(isTransferred) res.status(200).json(isTransferred);
  else res.status(401).json(isTransferred);
});

app.listen(process.env.PORT || PORT, async (err) => {
  if(!err) {
	console.log(`Server is listening on port ${PORT}`);
  } else {
	console.log(err);
  }
});
